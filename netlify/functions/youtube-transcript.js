const YOUTUBE_WATCH_URL = 'https://www.youtube.com/watch?v=';

function jsonResponse(statusCode, body) {
  return {
    statusCode,
    headers: {
      'Content-Type': 'application/json',
      'Access-Control-Allow-Origin': '*',
      'Cache-Control': 'no-store'
    },
    body: JSON.stringify(body)
  };
}

function decodeHtmlEntities(value) {
  return String(value || '')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&#x27;/g, "'")
    .replace(/&#x2F;/g, '/');
}

function extractVideoId(raw) {
  const value = String(raw || '').trim();
  if (/^[a-zA-Z0-9_-]{11}$/.test(value)) return value;

  try {
    const url = new URL(value);
    if (url.hostname.includes('youtu.be')) return url.pathname.slice(1, 12);
    return url.searchParams.get('v');
  } catch {
    return null;
  }
}

function parseCaptionTracks(html) {
  const match = html.match(/"captionTracks":(\[.*?\])\s*,\s*"audioTracks"/);
  if (!match) return [];

  const json = match[1]
    .replace(/\\"/g, '"')
    .replace(/\\u0026/g, '&');

  try {
    return JSON.parse(json);
  } catch {
    return [];
  }
}

function chooseCaptionTrack(tracks) {
  if (!Array.isArray(tracks) || tracks.length === 0) return null;

  return (
    tracks.find((track) => track.languageCode === 'en' && !track.kind) ||
    tracks.find((track) => track.languageCode === 'en') ||
    tracks.find((track) => String(track.languageCode || '').startsWith('en')) ||
    tracks[0]
  );
}

function parseTranscriptXml(xml) {
  const entries = [];
  const textRegex = /<text\b([^>]*)>([\s\S]*?)<\/text>/g;
  let match;

  while ((match = textRegex.exec(xml)) !== null) {
    const attrs = match[1] || '';
    const startMatch = attrs.match(/\bstart="([^"]+)"/);
    const durMatch = attrs.match(/\bdur="([^"]+)"/);
    const text = decodeHtmlEntities(match[2]).replace(/\s+/g, ' ').trim();

    if (text) {
      entries.push({
        start: startMatch ? Number(startMatch[1]) : null,
        duration: durMatch ? Number(durMatch[1]) : null,
        text
      });
    }
  }

  return entries;
}

exports.handler = async (event) => {
  if (event.httpMethod === 'OPTIONS') {
    return jsonResponse(200, { ok: true });
  }

  const videoId = extractVideoId(event.queryStringParameters?.videoId || event.queryStringParameters?.url);
  if (!videoId) {
    return jsonResponse(400, { error: 'A valid YouTube video ID or URL is required.' });
  }

  try {
    const watchRes = await fetch(`${YOUTUBE_WATCH_URL}${encodeURIComponent(videoId)}`, {
      headers: {
        'User-Agent': 'Mozilla/5.0 SermonAI transcript fetcher'
      }
    });

    if (!watchRes.ok) {
      return jsonResponse(watchRes.status, { error: 'Could not load the YouTube watch page.' });
    }

    const html = await watchRes.text();
    const tracks = parseCaptionTracks(html);
    const track = chooseCaptionTrack(tracks);

    if (!track?.baseUrl) {
      return jsonResponse(404, {
        error: 'No captions or transcript were found for this YouTube video. Paste the transcript manually for exact summarization.'
      });
    }

    const transcriptUrl = `${track.baseUrl.replace(/\\u0026/g, '&')}&fmt=srv3`;
    const transcriptRes = await fetch(transcriptUrl, {
      headers: {
        'User-Agent': 'Mozilla/5.0 SermonAI transcript fetcher'
      }
    });

    if (!transcriptRes.ok) {
      return jsonResponse(transcriptRes.status, { error: 'Could not download the YouTube captions.' });
    }

    const xml = await transcriptRes.text();
    const entries = parseTranscriptXml(xml);
    const transcript = entries.map((entry) => entry.text).join(' ').replace(/\s+/g, ' ').trim();

    if (!transcript) {
      return jsonResponse(404, { error: 'The caption track was empty. Paste the transcript manually for exact summarization.' });
    }

    return jsonResponse(200, {
      videoId,
      language: track.languageCode || 'unknown',
      isAutoGenerated: track.kind === 'asr',
      transcript,
      entries
    });
  } catch (error) {
    return jsonResponse(500, {
      error: error.message || 'Transcript extraction failed.'
    });
  }
};
